import java.util.Scanner;

public class Tc2 {
    public static void main(String[] args) {
        
        //tc=O(n)
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        for(int i=0;i<n;i++){
            System.out.println("Hello");
        }
        //tc=O(n)
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.println("hey");
            }
        }
    }
}
